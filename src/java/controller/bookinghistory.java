    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        Account ThisAccount = (Account)session.getAttribute("loggedInAccount");
        String identityCard = ThisAccount.getIdentityCard();

        if (identityCard != null && !identityCard.isEmpty()) {
            BookDAO bookDAO = new BookDAO();
            List<Book> bookList = bookDAO.getBookList(identityCard);
            request.setAttribute("bookList", bookList);
        }

        // Forward to the JSP page that displays the booking history
        request.getRequestDispatcher("/bookinghistory.jsp").forward(request, response);
    }

